public class SysDeptDetail implements java.io.Serializable{

    private static final long serialVersionUID = 1L;
    /**
    *主键
    */
    private Integer id;
    /**
    *内码
    */
    private String nm;
    /**
    *短编码
    */
    private String scode;
    /**
    *全编码
    */
    private String fcode;
    /**
    * 上级ID
    */
    private Integer super_id;

    /**
    *单位部门名称
    */
    private String name;
    /**
     *备注
     */
     private String remark;
    /**
    *状态
    */
    private Integer flag;

    private String super_name;
    /** default constructor */
    public SysDeptDetail() {
    
    }
    
    //属性get/set定义       		

    /**
      *主键
    */
    public  Integer getId() {
        return this.id;
    }
    public void setId( Integer id) {
        this.id = id;
    }        

    /**
      *内码
    */
    public  String getNm() {
        return this.nm;
    }
    public void setNm( String nm) {
        this.nm = nm;
    }        

    public String getScode() {
		return scode;
	}

	public void setScode(String scode) {
		this.scode = scode;
	}

	public String getFcode() {
		return fcode;
	}

	public void setFcode(String fcode) {
		this.fcode = fcode;
	}


    public Integer getSuper_id() {
        return super_id;
    }

    public void setSuper_id(Integer super_id) {
        this.super_id = super_id;
    }

    public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	/**
      *单位部门名称
    */
    public  String getName() {
        return this.name;
    }
    public void setName( String name) {
        this.name = name;
    }        

    /**
      *状态
    */
    public  Integer getFlag() {
        return this.flag;
    }
    public void setFlag( Integer flag) {
        this.flag = flag;
    }

    public String getSuper_name() {
        return super_name;
    }

    public void setSuper_name(String super_name) {
        this.super_name = super_name;
    }
}