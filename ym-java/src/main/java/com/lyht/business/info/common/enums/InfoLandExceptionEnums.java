package com.lyht.business.info.common.enums;

import com.lyht.base.common.enums.ExceptionEnums;

/**
 * 	lyht系统异常枚举
 * 
 * @author hxl
 *
 */
public enum InfoLandExceptionEnums implements ExceptionEnums{
	INVALID_LEVEL("-7102", "无效的级别");

	private String code;
	private String msg;

	InfoLandExceptionEnums(String code, String msg) {
		this.code = code;
		this.msg = msg;
	}

	@Override
	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
