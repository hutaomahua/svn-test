package com.lyht.business.land.bean;

import com.alibaba.fastjson.JSON;

import java.sql.Date;

/**
 * 用地报批存在问题
 * 
 * @author hxl
 *
 */
public class LandProblemDetail {

	private Integer id;

	private String nm;

	private String landNm;

	private String pText;

	private String problem;

	private String createStaffNm;

	private Date createTime;
	
	private String createStaff;

	/**
	 * 字典统计数
	 */
	private Integer countNm;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getNm() {
		return nm;
	}

	public void setNm(String nm) {
		this.nm = nm;
	}

	public String getProblem() {
		return problem;
	}

	public void setProblem(String problem) {
		this.problem = problem;
	}

	public String getLandNm() {
		return landNm;
	}

	public void setLandNm(String landNm) {
		this.landNm = landNm;
	}

	public String getpText() {
		return pText;
	}

	public void setpText(String pText) {
		this.pText = pText;
	}

	public String getCreateStaffNm() {
		return createStaffNm;
	}

	public void setCreateStaffNm(String createStaffNm) {
		this.createStaffNm = createStaffNm;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getCreateStaff() {
		return createStaff;
	}

	public void setCreateStaff(String createStaff) {
		this.createStaff = createStaff;
	}

	@Override
	public String toString() {
		return JSON.toJSONString(this);
	}

	public Integer getCountNm() {
		return countNm;
	}

	public void setCountNm(Integer countNm) {
		this.countNm = countNm;
	}
}