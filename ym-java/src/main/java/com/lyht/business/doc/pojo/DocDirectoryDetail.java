package com.lyht.business.doc.pojo;

import java.util.List;

public class DocDirectoryDetail {

	private Integer key;

	private String value;

	private String title;
	
	private List<DocDirectoryDetail> children;

	public Integer getKey() {
		return key;
	}

	public String getValue() {
		return value;
	}

	public String getTitle() {
		return title;
	}


	public void setKey(Integer key) {
		this.key = key;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public List<DocDirectoryDetail> getChildren() {
		return children;
	}

	public void setChildren(List<DocDirectoryDetail> children) {
		this.children = children;
	}

	
	

}
