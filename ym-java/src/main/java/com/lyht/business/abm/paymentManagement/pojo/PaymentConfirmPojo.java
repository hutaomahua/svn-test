package com.lyht.business.abm.paymentManagement.pojo;

import java.util.List;

import io.swagger.annotations.ApiModelProperty;

public class PaymentConfirmPojo {

	@ApiModelProperty(value = "行政区")
	private String region;

	@ApiModelProperty(value = "操作人")
	private String porposer;

	@ApiModelProperty(value = "操作时间")
	private String confirmTime;

	@ApiModelProperty(value = "备注")
	private String remark;
	
	@ApiModelProperty(value = "进行兑付确认的兑付申请记录内码")
	private List<String> data;

	public String getRegion() {
		return region;
	}

	public void setRegion(String region) {
		this.region = region;
	}

	public String getPorposer() {
		return porposer;
	}

	public void setPorposer(String porposer) {
		this.porposer = porposer;
	}

	public String getConfirmTime() {
		return confirmTime;
	}

	public void setConfirmTime(String confirmTime) {
		this.confirmTime = confirmTime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public List<String> getData() {
		return data;
	}

	public void setData(List<String> data) {
		this.data = data;
	}
	
}
